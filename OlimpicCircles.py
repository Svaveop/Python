import turtle

t = turtle.Turtle()
s = t.getscreen()
t.shape("turtle")
s.bgcolor("grey")

t.pencolor("blue")
t.circle(50)
t.penup()
t.forward(70)
t.pendown()
t.pencolor("black")
t.circle(50)
t.penup()
t.forward(70)
t.pendown()
t.pencolor("red")
t.circle(50)
t.penup()
t.goto(35, -70)
t.pendown()
t.pencolor("yellow")
t.circle(50)
t.penup()
t.forward(70)
t.pendown()
t.pencolor("green")
t.circle(50)
